# get a list of nocs starts with a specific number
def nocs_start_with(start_with: Union[list[str], str]):
    if type(start_with) == str:
        start_with = [start_with]

    data = []
    for start_str in start_with:
        sub_data = (
            [code for code in noc_2021_v1 if code.startswith(start_str)]
            if start_str.upper() != "ALL"
            else noc_2021_v1
        )

        data = [*data, *sub_data]

    return data
